    public static String getHostName() {
        try
        {
            return InetAddress.getLocalHost().getHostName();
        }
        catch (UnknownHostException e)
        {
        }
        return "未知";
    }

    /**
     * 根据 IP 获取所在地
     * 使用 阿里云 组件
     * @param ip
     * @return
     */
    public static String getAddrByIp(String ip){
        if (StringUtils.isEmpty(ip)) {
            return null;
        }
        if (LOCALHOST.equals(ip)) {
            return "本机访问";
        }
        String host = "https://hcapi22.market.alicloudapi.com";
        String path = "/ip";
        String method = "GET";
        Map<String, String> headers = new HashMap<String, String>();
        //最后在header中的格式(中间是英文空格)为Authorization:APPCODE 83359fd73fe94948385f570e3c139105
        // "Authorization", "APPCODE " + appcode
        headers.put("Authorization", "APPCODE " + "e49d0193d719433daaf9bf7000cb5c63");
        Map<String, String> querys = new HashMap<String, String>();
        querys.put("ip", ip);

        StringBuilder sb = new StringBuilder();

        try {
            HttpResponse response = HttpUtils.doGet(host, path, method, headers, querys);

            String body = EntityUtils.toString(response.getEntity());
            JSONObject JsonObject = JsonUtils.toJsonObject(body);
            int code = JsonObject.getInteger("ret");
            // 成功
            if (code == HttpStatus.SC_OK) {
                JSONObject data = JsonObject.getJSONObject("data");
                sb.append(data.getString("country") + ",");
                sb.append(data.getString("city") + ",");
                sb.append(data.getString("region"));
            }
        } catch (Exception e) {
            log.error(e.getMessage(),e);
        }
        return sb.toString();
    }

    public static void main(String[] args) {
        System.out.println(getAddrByIp("115.183.34.21"));
    }
